# Script de Rollback et Push vers GitHub
# Auteur: GitHub Copilot
# Objectif: Annuler le dernier commit sur Azure DevOps et le pousser sur GitHub

Write-Host "??????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?                                                                ?" -ForegroundColor Cyan
Write-Host "?     Rollback Azure DevOps ? Push vers GitHub                  ?" -ForegroundColor Cyan
Write-Host "?                                                                ?" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

# Vérifier que nous sommes dans le bon répertoire
if (-not (Test-Path "Jellyfin.Xtream.V2.csproj")) {
    Write-Host "? Vous n'ętes pas dans le répertoire du projet" -ForegroundColor Red
    exit 1
}

Write-Host "?? Répertoire du projet: OK" -ForegroundColor Green
Write-Host ""

# Étape 1: Vérifier l'état actuel
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host "ÉTAPE 1: Vérification de l'état actuel" -ForegroundColor Yellow
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""

Write-Host "Dernier commit:" -ForegroundColor Cyan
git log --oneline -1
Write-Host ""

Write-Host "Remotes actuels:" -ForegroundColor Cyan
git remote -v
Write-Host ""

# Étape 2: Demander confirmation pour le rollback
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host "ÉTAPE 2: Confirmation du rollback" -ForegroundColor Yellow
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""

$commitHash = git rev-parse HEAD
Write-Host "Commit ŕ annuler: $commitHash" -ForegroundColor Yellow
Write-Host "Message: feat: Optimisations majeures de performance pour haute volumétrie" -ForegroundColor Yellow
Write-Host ""

$response = Read-Host "Ętes-vous sűr de vouloir annuler ce commit? (O/N)"
if ($response -ne 'O' -and $response -ne 'o') {
    Write-Host "? Rollback annulé" -ForegroundColor Red
    exit 0
}

Write-Host ""

# Étape 3: Rollback du commit
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host "ÉTAPE 3: Rollback du commit" -ForegroundColor Yellow
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""

Write-Host "Annulation du commit local..." -ForegroundColor Cyan
git reset --soft HEAD~1

if ($LASTEXITCODE -eq 0) {
    Write-Host "? Commit annulé localement" -ForegroundColor Green
    Write-Host "   Les fichiers sont conservés en staging" -ForegroundColor Green
} else {
    Write-Host "? Erreur lors du rollback local" -ForegroundColor Red
    exit 1
}

Write-Host ""

# Étape 4: Forcer le push sur Azure DevOps pour annuler le commit distant
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host "ÉTAPE 4: Annulation du commit sur Azure DevOps" -ForegroundColor Yellow
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""

Write-Host "Reset de la branche distante..." -ForegroundColor Cyan
$response = Read-Host "Force push pour annuler le commit sur Azure DevOps? (O/N) - ATTENTION: Cette action est irréversible"
if ($response -ne 'O' -and $response -ne 'o') {
    Write-Host "??  Force push annulé - Le commit reste sur Azure DevOps" -ForegroundColor Yellow
} else {
    git push origin MIV_DEV01_TestPush --force-with-lease

    if ($LASTEXITCODE -eq 0) {
        Write-Host "? Branche distante annulée" -ForegroundColor Green
    } else {
        Write-Host "??  Erreur lors du force push" -ForegroundColor Yellow
    }
}

Write-Host ""

# Étape 5: Configurer GitHub
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host "ÉTAPE 5: Configuration de GitHub comme remote" -ForegroundColor Yellow
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""

$githubUrl = Read-Host "Entrez l'URL de votre repository GitHub (ex: https://github.com/username/Jellyfin.Xtream.V2.git)"

if ([string]::IsNullOrEmpty($githubUrl)) {
    Write-Host "? URL GitHub manquante" -ForegroundColor Red
    exit 1
}

Write-Host "Configuration du remote 'github'..." -ForegroundColor Cyan

# Vérifier si le remote existe déjŕ
$remoteExists = git remote | Select-String -Pattern "github" -Quiet
if ($remoteExists) {
    Write-Host "Remote 'github' existe déjŕ, mise ŕ jour..." -ForegroundColor Yellow
    git remote set-url github $githubUrl
} else {
    Write-Host "Création du remote 'github'..." -ForegroundColor Cyan
    git remote add github $githubUrl
}

if ($LASTEXITCODE -eq 0) {
    Write-Host "? Remote 'github' configuré" -ForegroundColor Green
} else {
    Write-Host "? Erreur lors de la configuration du remote" -ForegroundColor Red
    exit 1
}

Write-Host ""

# Étape 6: Afficher les remotes
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host "Remotes configurés:" -ForegroundColor Yellow
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""
git remote -v
Write-Host ""

# Étape 7: Créer une branche GitHub ou choisir la branche de destination
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host "ÉTAPE 6: Sélection de la branche GitHub" -ForegroundColor Yellow
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""

Write-Host "Branches actuelles:" -ForegroundColor Cyan
git branch -a
Write-Host ""

$targetBranch = Read-Host "Sur quelle branche GitHub voulez-vous pousser? (ex: main, develop, feature/performance)"
if ([string]::IsNullOrEmpty($targetBranch)) {
    $targetBranch = "main"
    Write-Host "Branche par défaut: main" -ForegroundColor Yellow
}

Write-Host ""

# Étape 8: Pousser vers GitHub
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host "ÉTAPE 7: Push vers GitHub" -ForegroundColor Yellow
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""

Write-Host "Statut du repository avant push:" -ForegroundColor Cyan
git status
Write-Host ""

$response = Read-Host "Ętes-vous pręt ŕ pousser vers GitHub? (O/N)"
if ($response -ne 'O' -and $response -ne 'o') {
    Write-Host "??  Push annulé" -ForegroundColor Yellow
    Write-Host "   Les fichiers modifiés sont conservés" -ForegroundColor Yellow
    exit 0
}

Write-Host ""
Write-Host "Push vers GitHub..." -ForegroundColor Cyan

# Si on n'est pas sur la branche cible, créer/basculer
$currentBranch = git rev-parse --abbrev-ref HEAD
if ($currentBranch -ne $targetBranch) {
    Write-Host "Création/basculement vers la branche $targetBranch..." -ForegroundColor Yellow
    git checkout -b $targetBranch 2>$null
    git checkout $targetBranch 2>$null
}

# Push vers GitHub
git push github $targetBranch --set-upstream

if ($LASTEXITCODE -eq 0) {
    Write-Host ""
    Write-Host "??????????????????????????????????????????????????????????????????" -ForegroundColor Green
    Write-Host "?                                                                ?" -ForegroundColor Green
    Write-Host "?  ? SUCCČS - Changements pushés vers GitHub!                  ?" -ForegroundColor Green
    Write-Host "?                                                                ?" -ForegroundColor Green
    Write-Host "??????????????????????????????????????????????????????????????????" -ForegroundColor Green
    Write-Host ""
    Write-Host "Résumé:" -ForegroundColor Cyan
    Write-Host "  ? Commit annulé sur Azure DevOps" -ForegroundColor Green
    Write-Host "  ? Remote GitHub configuré" -ForegroundColor Green
    Write-Host "  ? Changements pushés vers GitHub ($targetBranch)" -ForegroundColor Green
    Write-Host ""
    Write-Host "URL GitHub: $githubUrl" -ForegroundColor Yellow
    Write-Host "Branche: $targetBranch" -ForegroundColor Yellow
    Write-Host ""
} else {
    Write-Host ""
    Write-Host "? Erreur lors du push vers GitHub" -ForegroundColor Red
    Write-Host "   Vérifiez votre URL et vos credentials GitHub" -ForegroundColor Yellow
    exit 1
}

Write-Host "Appuyez sur une touche pour fermer..." -ForegroundColor Gray
$null = $Host.UI.RawUI.ReadKey("NoEcho,IncludeKeyDown")
